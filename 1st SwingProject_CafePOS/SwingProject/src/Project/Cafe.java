package Project;

public class Cafe {
	private int num;
	private String time;
	private String menu;
	private String temperture;
	private int price;
	private String pay;
	private int countMenu;
	private int sumPrice;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getMenu() {
		return menu;
	}
	public void setMenu(String menu) {
		this.menu = menu;
	}
	public String getTemperture() {
		return temperture;
	}
	public void setTemperture(String temperture) {
		this.temperture = temperture;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public int getSumPrice() {
		return sumPrice;
	}
	public void setSumPrice(int sumPrice) {
		this.sumPrice = sumPrice;
	}
	public int getCountMenu() {
		return countMenu;
	}
	public void setCountMenu(int countMenu) {
		this.countMenu = countMenu;
	}
	
	
}
